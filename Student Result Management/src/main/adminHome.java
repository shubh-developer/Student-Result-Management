    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String rollno = jTextField3.getText();
        String course = (String)jComboBox1.getSelectedItem();
        String branch = (String)jComboBox2.getSelectedItem();
        String gender = (String)jComboBox3.getSelectedItem();
        String name = jTextField4.getText();
        String fatherName =jTextField5.getText();
        
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms", "root", "root");
            Statement st = con.createStatement();
            
            st.executeUpdate("insert into student(rollno,course,branchName,name,gender,fathername) values('"+rollno+"','"+course+"','"+branch+"','"+name+"','"+gender+"','"+fatherName+"')");
            JOptionPane.showMessageDialog(null, "Data Saved Succesfully !!");
            
            setVisible(false);
            new adminHome().setVisible(true);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_jButton6ActionPerformed

